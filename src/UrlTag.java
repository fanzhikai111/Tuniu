public class UrlTag {
	
	String url;
	String tag;

	/**
	 * @param url
	 * @param tag
	 */
	public UrlTag(String url, String tag) {
		super();
		this.url = url;
		this.tag = tag;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public String getTag() {
		return tag;
	}

	public void setTag(String tag) {
		this.tag = tag;
	}
}
